public class PlayerDefinition
{
  public String name;
  public String hairLength;
  public String glasses;
  public String facialHair;
  public String eyeColor;
  public String pimples;
  public String hat;
  public String hairColor;
  public String noseShape;
  public String faceShape;

  public PlayerDefinition(String name, String hairLength, String glasses, String facialHair, String eyeColor, String pimples, String hat, String hairColor, String noseShape, String faceShape){
    this.name = name;
    this.hairLength = hairLength;
    this.glasses = glasses;
    this.facialHair = facialHair;
    this.eyeColor = eyeColor;
    this.pimples = pimples;
    this.hat = hat;
    this.hairColor = hairColor;
    this.noseShape = noseShape;
    this.faceShape = faceShape;
  }

  public PlayerDefinition()
  {

  }

}
